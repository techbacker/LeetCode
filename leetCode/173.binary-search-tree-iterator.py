# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class BSTIterator:

    def __init__(self, root: TreeNode):
        self.stack = []
        self.push_from_top(root)

    def push_from_top(self, node):
        while node:
            self.stack.append(node)
            node = node.left

    def next(self) -> int:
        """
        @return the next smallest number
        """
        node = self.stack.pop()
        result = node.val
        if node.right:
            node = node.right
            self.push_from_top(node)
        return result


    def hasNext(self) -> bool:
        """
        @return whether we have a next smallest number
        """
        return True if len(self.stack) > 0 else False

